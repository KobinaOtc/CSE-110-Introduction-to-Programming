# Loop through the strings

colors = ["red", "blue", "green", "yellow"]

# Print all colors individually
for color in colors:
    print(color)
print()

# print number in range
for num in range(1, 9):
    print(num)
print()

# Print number in range
for num in range(1, 21):
    if num % 2 == 0:
        print(num)
print()

# Print even numbers
for num in range(2, 21, 2):
    print(num)
print()